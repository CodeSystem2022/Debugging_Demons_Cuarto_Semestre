# Sistema decimal
a = 10
print(f'Valor decimal: {a}')

# Sistema binario
a = 0b1010
print(f'Valor binario: {a}')

# Sistema octal
a = 0o12
print(f'Valor octal: {a}')

# Sistema hexadecimal
a = 0xA
print(f'Valor hexadecimal: {a}')

# Base decimal
a = int('23', 10)
print(f'a = base decimal {a}')

# Base binario
a = int('10111', 2)
print(f'a = base binario {a}')

# Base octal
a = int('27', 8)
print(f'a = base octal {a}')

# Base hexadecimal
a = int('17', 16)
print(f'a = base hexadecimal {a}')

