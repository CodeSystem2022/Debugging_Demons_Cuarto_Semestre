# Sistema binario
a = 0b1010
print(f"a binario: {a}")

# Sistema octal
a = 0o12
print(f"a octal: {a}")

# Sistema hexadecimal
a = 0xA
print(f"hexadecimal: {a}")

# Base Decimal
a = int('23', 10)
print(f"a = base decimal: {a}")

# Base Binario
a = int('1010', 2)
print(f"a = base binario: {a}")

# Base Octal
a = int('12', 8)
print(f"a = base octal: {a}")

# Base Hexadecimal
a = int('A', 16)
print(f"a = base hexadecimal: {a}")

# Base 5 sus valores son de 0 a 4
a = int('34', 5)
