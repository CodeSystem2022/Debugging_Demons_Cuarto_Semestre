#Profundizando en los sistemas de numeracion

#Sistema decimal

a = 10
print(f'a decimal: {a}')
# Sistema binario
a = 0b1010
print(f'a binario: {a}')
# Sistema octal

a = 0o12
print(f'a octal: {a}')

# Siste hexadecimal

a = 0xA
print(f'a hexadecimal: {a}')


# Base decimal
a = int('23', 10)
print(f'a = base decimal: {a}')

# Base binaria
a = int('10111', 2)
print(f'a = base binario: {a}')

# Base octal
a = int('27', 8)
print(f'a = base octal: {a}')

# Base hexadecimal
a = int('17', 16)
print(f'a = base hexadecimal: {a}')

# Base 5 (sus valores son de 0-4)
a = int('43', 5)
print(f'a = base cinco: {a}')
